package basicJava;

public class DataType {
	

	
	
	
	
	
	
	public static void main(String[] args) {
	
		
		
		
		//DataType
		
		//int
		int	a = 100;	//int for whole numbers
		int	b = 200;
		
			System.out.println(  "a + b : " + (a+b) );
	
		//float
		float c = 10.5f;	// float for decimal value
		float d = 20.5f;
		
			System.out.println("c + d : " + (c+d));
		
		//String
		String firstName = "Raj";
		
		String lastName  = "kumar";
		
			System.out.println("UserName : "   + (firstName + lastName));
		
		//Long
			
		long negative = -9223372036854775808L;
		
		long positive =  9223372036854775807L;
		
			System.out.println("negative value of long " + negative);
			System.out.println("positive value of long " + positive);
		
		//char - one letter
		
		char initial = 'k';
		
		String name = "Karthi";
		
			System.out.println("name  : " + (initial + " " + name));
			
			
		//double
		
		double doubleValue = 12E4d;
			
			System.out.println("doubleValue :" + doubleValue);
			
			
		//Boolean
			
		boolean ans = 25 < 10 ;
		
			System.out.println("25 > 10 : " + ans);
		
		
	}
	
	
	
	
	
}
